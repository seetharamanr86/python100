
current_age = int(input("Enter you current age: "))

target_age = 90

left_yrs = target_age - current_age

left_months = left_yrs * 12

left_weeks = left_yrs * 52

left_days = left_yrs * 365

print(f"You have {left_days} days, {left_weeks} weeks, {left_months} months and {left_yrs} years left on your life")


